public class Bitwise {
	public static void main(String[] args) {
		System.out.println(Integer.toBinaryString(-1));
		System.out.println((-1)>> 4);
		System.out.println(Integer.toBinaryString((-1)>> 4));
		System.out.println((-1)>>> 4);
		System.out.println(Integer.toBinaryString((-1)>>> 4));
	}
}
